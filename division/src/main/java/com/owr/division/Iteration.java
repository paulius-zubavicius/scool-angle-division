package com.owr.division;

public class Iteration {

	private long remainder;
	private long liftedDown;
	private long origRemainder;
	private long intPart;
	private long subs;
	private long result;

	public long getRemainder() {
		return remainder;
	}

	public void setRemainder(long remainder) {
		this.remainder = remainder;
	}

	public long getLiftedDown() {
		return liftedDown;
	}

	public void setLiftedDown(long liftedDown) {
		this.liftedDown = liftedDown;
	}

	public long getOrigRemainder() {
		return origRemainder;
	}

	public void setOrigRemainder(long origRemainder) {
		this.origRemainder = origRemainder;
	}

	public long getIntPart() {
		return intPart;
	}

	public void setIntPart(long intPart) {
		this.intPart = intPart;
	}

	public long getSubs() {
		return subs;
	}

	public void setSubs(long subs) {
		this.subs = subs;
	}

	public long getResult() {
		return result;
	}

	public void setResult(long result) {
		this.result = result;
	}

}
